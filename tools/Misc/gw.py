import math

# Realistic sliding door motor parameters
motor_voltage = 24        # Volts
motor_rpm = 60            # RPM under load
gear_ratio = 50            # Gearbox reduction
roller_friction = 5       # N along track
door_mass = 100           # kg per leaf
external_force = 200      # N applied along track

# Motor specifications for heating calculation
motor_resistance = 0.5           # Ohms, winding resistance
motor_max_temp = 80              # Celsius, max winding temperature
ambient_temp = 25                # Celsius
motor_thermal_resistance = 0.5   # °C/W, thermal resistance to ambient
motor_heat_capacity = 1000       # J/°C, realistic motor mass

# Steel conduction to motor housing/track
steel_thermal_conductance = 50    # W/°C, approximate steel to steel contact

# Realistic torque limit from max current
motor_current_max = motor_voltage / motor_resistance  # A
motor_torque_limit = 50          # Nm at shaft, realistic

# Torque at door shaft after gearbox
torque_door = motor_torque_limit * gear_ratio
pulley_radius = 0.05              # meters
motor_force = torque_door / pulley_radius
net_force = motor_force - external_force - roller_friction

# Heat generated by holding torque (I^2 * R)
power_loss = motor_current_max**2 * motor_resistance   # Watts

# Steady-state temperature rise via steel conduction
delta_temp_steady = power_loss / steel_thermal_conductance  # °C above ambient
thermal_equilibrium_temp = ambient_temp + delta_temp_steady

# Time to reach max temperature from ambient
delta_temp = motor_max_temp - ambient_temp
time_to_overheat = (motor_heat_capacity * delta_temp) / power_loss  # seconds

print(f"Motor torque at shaft: {motor_torque_limit:.2f} Nm")
print(f"Torque at door shaft: {torque_door:.2f} Nm")
print(f"Force along track at door edge: {motor_force:.1f} N")
print(f"Net force against external push: {net_force:.1f} N")
print(f"Steady-state temperature rise if conduction to steel: {delta_temp_steady:.1f} °C")
print(f"Thermal equilibrium temperature via steel conduction: {thermal_equilibrium_temp:.1f} °C")
print(f"Estimated time before motor overheats: {time_to_overheat/60:.1f} minutes")

if thermal_equilibrium_temp > motor_max_temp:
    print("Motor will overheat at steady state")
else:
    print("Motor can reach thermal equilibrium without exceeding max temperature")

if net_force > 0:
    print("Motor could resist this force in principle, but only momentarily; DC motor is not designed for continuous holding")
else:
    print("Motor cannot hold the door against this external force")

